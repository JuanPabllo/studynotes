# Comparando objetos com o operador !=

print(1 != 2)
print(6 != 6)

# Comparando objetos com o operador <

print(7 < 5)
print(7 < 14)

# Comparando objetos com o operador >

print(7 > 5)
print(7 > 14)

# Comparando objetos com o operador >=

print(700 >= 500)
print(700 >= 850)

# Operação	Comparação
# x == y	x é igual a y?
# x != y	x é diferente de y?
# x > y	x é maior que y?
# x < y	x é menor que y?
# x >= y	x é maior ou igual a y?
# x <= y	x é menor ou igual a y?
